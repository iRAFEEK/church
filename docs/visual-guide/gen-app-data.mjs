// Generates lib/help/guide-data.ts from the manifest + public/help-guide media inventory.
import { writeFileSync, existsSync } from 'node:fs'
import { SECTIONS } from './manifest.mjs'
import { EN_CATEGORIES, EN_SECTIONS } from './translations-en.mjs'
const M = 'public/help-guide'
const has = (f)=>existsSync(`${M}/${f}`)
const num = (n)=>parseInt(String(n))

const CATS = [
  { id:'welcome',  icon:'👋', title:'أهلًا بيك',          from:1,  to:2,  minRole:'member' },
  { id:'start',    icon:'🚪', title:'أول خطوة',           from:3,  to:11, minRole:'member' },
  { id:'member',   icon:'🏠', title:'للأعضاء',            from:12, to:25, minRole:'member' },
  { id:'leader',   icon:'👨‍👩‍👧‍👦', title:'لقائد المجموعة', from:26, to:31, minRole:'group_leader' },
  { id:'admin',    icon:'🛠️', title:'للمشرفين والقادة',   from:32, to:50, minRole:'ministry_leader' },
  { id:'settings', icon:'⚙️', title:'إعدادات الكنيسة',    from:51, to:54, minRole:'super_admin' },
  { id:'ref',      icon:'📖', title:'مرجع سريع',          from:55, to:59, minRole:'member' },
]
const lessonRoleOverride = { '55':'super_admin' }

const cats = CATS.map(c=>({
  id: c.id, icon: c.icon, title: c.title, minRole: c.minRole,
  ...(EN_CATEGORIES[c.id] ? {titleEn: EN_CATEGORIES[c.id]} : {}),
  lessons: SECTIONS.filter(s=>num(s.n)>=c.from && num(s.n)<=c.to).map(s=>({
    id: String(s.n), icon: s.icon, title: s.t,
    ...(EN_SECTIONS[String(s.n)]?.t ? {titleEn: EN_SECTIONS[String(s.n)].t} : {}),
    minRole: lessonRoleOverride[String(s.n)] || c.minRole,
    steps: s.steps.map((st,i)=>({
      icon: st.icon, ar: st.ar,
      ...(EN_SECTIONS[String(s.n)]?.steps?.[i] ? {en: EN_SECTIONS[String(s.n)].steps[i]} : {}),
      ...(has(`s${s.n}-${i}.jpg`) ? {img:`/help-guide/s${s.n}-${i}.jpg`} : {}),
      ...(has(`v-s${s.n}-${i}.mp4`) ? {vid:`/help-guide/v-s${s.n}-${i}.mp4`} : {}),
      ...(has(`a-s${s.n}-${i}.m4a`) ? {aud:`/help-guide/a-s${s.n}-${i}.m4a`} : {}),
    })),
    ...(has(`a-s${s.n}-title.m4a`) ? {titleAud:`/help-guide/a-s${s.n}-title.m4a`} : {}),
    ...(has(`s${s.n}-0.jpg`) ? {thumb:`/help-guide/s${s.n}-0.jpg`} : {}),
  })),
}))

const PATH_LESSONS = [
  ['/admin/settings/qr','37'],['/admin/settings/roles','52'],['/admin/settings/privacy','53'],
  ['/admin/settings/notifications','54'],['/admin/settings','51'],
  ['/admin/members','33'],['/admin/visitors','36'],['/admin/groups','38'],['/admin/ministries','39'],
  ['/admin/events','40'],['/admin/templates','41'],['/admin/calendar','42'],['/admin/prayers','45'],
  ['/admin/songs','21'],['/admin/outreach','47'],['/admin/locations','48'],['/admin/join-requests','50'],
  ['/my-group','26'],['/visitors','31'],['/groups','24'],
  ['/events','15'],['/serving','16'],['/announcements','17'],['/prayer','18'],['/bible','19'],
  ['/liturgy','20'],['/bookings','22'],['/community/needs','23'],['/notifications','14'],
  ['/profile','13'],['/dashboard','12'],
]

const ts = `// AUTO-GENERATED by docs/visual-guide/gen-app-data.mjs — do not edit by hand.
// Content dataset for the in-app visual guide (Arabic-first, low-literacy audience).
// \`en\`/\`titleEn\` are optional English text; audio/video voiceovers stay Arabic-only.
import type { UserRole } from '@/types'

export interface GuideStep { icon: string; ar: string; en?: string; img?: string; vid?: string; aud?: string }
export interface GuideLesson {
  id: string; icon: string; title: string; titleEn?: string; minRole: UserRole
  steps: GuideStep[]; titleAud?: string; thumb?: string
}
export interface GuideCategory {
  id: string; icon: string; title: string; titleEn?: string; minRole: UserRole; lessons: GuideLesson[]
}

const ROLE_RANK: Record<UserRole, number> = { member: 0, group_leader: 1, ministry_leader: 2, super_admin: 3 }
export const canSee = (role: UserRole, minRole: UserRole) => ROLE_RANK[role] >= ROLE_RANK[minRole]

export const GUIDE: GuideCategory[] = ${JSON.stringify(cats, null, 2)} as GuideCategory[]

export function findLesson(id: string): { lesson: GuideLesson; category: GuideCategory } | null {
  for (const c of GUIDE) { const l = c.lessons.find(x => x.id === id); if (l) return { lesson: l, category: c } }
  return null
}

// Longest-prefix route → lesson id (for the contextual Help button)
const PATH_LESSONS: [string, string][] = ${JSON.stringify(PATH_LESSONS)}
export function lessonIdForPath(pathname: string, role: UserRole): string | null {
  for (const [prefix, id] of PATH_LESSONS) {
    if (pathname === prefix || pathname.startsWith(prefix + '/')) {
      const hit = findLesson(id)
      if (hit && canSee(role, hit.lesson.minRole)) return id
    }
  }
  return null
}
`
writeFileSync('lib/help/guide-data.ts', ts)
console.log('lib/help/guide-data.ts generated:', Math.round(ts.length/1024), 'KB')
