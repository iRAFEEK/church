// Generates lib/help/guide-data.ts from the manifest + public/help-guide media inventory.
//
// LOCALIZATION MODEL (scales to any number of app languages):
//   Arabic is the source of truth (manifest.mjs: `t` titles + `ar` step sentences).
//   Each additional language lives in docs/visual-guide/translations-<locale>.mjs
//   (e.g. translations-en.mjs, translations-fr.mjs) exporting a categories map and a
//   sections map — export names may be anything ending in CATEGORIES / SECTIONS.
//   The generator merges every discovered locale into per-item text maps:
//     lesson.titles = { ar: '…', en: '…', … }   step.text = { ar: '…', en: '…', … }
//   The app picks via pickGuideText(map, appLocale): exact locale → base language
//   ('ar-eg' → 'ar') → Arabic fallback. Adding a language = adding ONE file here and
//   re-running `node docs/visual-guide/gen-app-data.mjs` — no component changes.
//   Voiceovers/videos are recorded Arabic media and stay locale-independent for now.
import { writeFileSync, existsSync, readdirSync } from 'node:fs'
import { SECTIONS } from './manifest.mjs'
const M = 'public/help-guide'
const has = (f)=>existsSync(`${M}/${f}`)
const num = (n)=>parseInt(String(n))

// ── Discover translation files: translations-<locale>.mjs ──
const here = new URL('.', import.meta.url).pathname
const translationFiles = readdirSync(here).filter(f => /^translations-[a-z-]+\.mjs$/.test(f))
const LOCALES = {} // locale -> { categories: {id:title}, sections: {n:{t, steps[]}} }
for (const f of translationFiles) {
  const locale = f.replace(/^translations-/, '').replace(/\.mjs$/, '')
  const mod = await import(`./${f}`)
  const catKey = Object.keys(mod).find(k => k.endsWith('CATEGORIES'))
  const secKey = Object.keys(mod).find(k => k.endsWith('SECTIONS'))
  if (!catKey || !secKey) { console.warn(`[gen] ${f}: missing *CATEGORIES/*SECTIONS exports — skipped`); continue }
  LOCALES[locale] = { categories: mod[catKey], sections: mod[secKey] }
}
console.log('[gen] locales merged:', ['ar', ...Object.keys(LOCALES)].join(', '))

// Build a text map { ar, <locale>: … } — ar always present, others only when translated.
const textMap = (ar, per) => {
  const m = { ar }
  for (const [loc, val] of Object.entries(per)) if (typeof val === 'string' && val.length) m[loc] = val
  return m
}

const CATS = [
  { id:'welcome',  icon:'👋', title:'أهلًا بيك',          from:1,  to:2,  minRole:'member' },
  { id:'start',    icon:'🚪', title:'أول خطوة',           from:3,  to:11, minRole:'member' },
  { id:'member',   icon:'🏠', title:'للأعضاء',            from:12, to:25, minRole:'member' },
  { id:'leader',   icon:'👨‍👩‍👧‍👦', title:'لقائد المجموعة', from:26, to:31, minRole:'group_leader' },
  { id:'admin',    icon:'🛠️', title:'للمشرفين والقادة',   from:32, to:50, minRole:'ministry_leader' },
  { id:'settings', icon:'⚙️', title:'إعدادات الكنيسة',    from:51, to:54, minRole:'super_admin' },
  { id:'ref',      icon:'📖', title:'مرجع سريع',          from:55, to:59, minRole:'member' },
]
const lessonRoleOverride = { '55':'super_admin' }

const perLocale = (fn) => Object.fromEntries(Object.entries(LOCALES).map(([loc, data]) => [loc, fn(data)]))

const cats = CATS.map(c=>({
  id: c.id, icon: c.icon, minRole: c.minRole,
  titles: textMap(c.title, perLocale(d => d.categories?.[c.id])),
  lessons: SECTIONS.filter(s=>num(s.n)>=c.from && num(s.n)<=c.to).map(s=>({
    id: String(s.n), icon: s.icon,
    titles: textMap(s.t, perLocale(d => d.sections?.[String(s.n)]?.t)),
    minRole: lessonRoleOverride[String(s.n)] || c.minRole,
    steps: s.steps.map((st,i)=>({
      icon: st.icon,
      text: textMap(st.ar, perLocale(d => d.sections?.[String(s.n)]?.steps?.[i])),
      ...(has(`s${s.n}-${i}.jpg`) ? {img:`/help-guide/s${s.n}-${i}.jpg`} : {}),
      ...(has(`v-s${s.n}-${i}.mp4`) ? {vid:`/help-guide/v-s${s.n}-${i}.mp4`} : {}),
      ...(has(`a-s${s.n}-${i}.m4a`) ? {aud:`/help-guide/a-s${s.n}-${i}.m4a`} : {}),
    })),
    ...(has(`a-s${s.n}-title.m4a`) ? {titleAud:`/help-guide/a-s${s.n}-title.m4a`} : {}),
    ...(has(`s${s.n}-0.jpg`) ? {thumb:`/help-guide/s${s.n}-0.jpg`} : {}),
  })),
}))

