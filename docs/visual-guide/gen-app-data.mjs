const ts = `// AUTO-GENERATED by docs/visual-guide/gen-app-data.mjs — do not edit by hand.
// Content dataset for the in-app visual guide (Arabic-first, low-literacy audience).
// Text is a per-locale map (ar always present; other locales merged from
// docs/visual-guide/translations-<locale>.mjs). Pick with pickGuideText(map, locale).
// Audio/video voiceovers are recorded Arabic media (locale-independent for now).
import type { UserRole } from '@/types'

/** Per-locale text: 'ar' is always present; other app locales are optional. */
export type GuideText = Record<string, string> & { ar: string }

export interface GuideStep { icon: string; text: GuideText; img?: string; vid?: string; aud?: string }
export interface GuideLesson {
  id: string; icon: string; titles: GuideText; minRole: UserRole
  steps: GuideStep[]; titleAud?: string; thumb?: string
}
export interface GuideCategory {
  id: string; icon: string; titles: GuideText; minRole: UserRole; lessons: GuideLesson[]
}

/** Resolve guide text for the current app locale: exact ('ar-eg') → base ('ar') → Arabic. */
export function pickGuideText(map: GuideText, locale: string): string {
  return map[locale] ?? map[locale.split('-')[0]] ?? map.ar
}

const ROLE_RANK: Record<UserRole, number> = { member: 0, group_leader: 1, ministry_leader: 2, super_admin: 3 }
export const canSee = (role: UserRole, minRole: UserRole) => ROLE_RANK[role] >= ROLE_RANK[minRole]

export const GUIDE: GuideCategory[] = ${JSON.stringify(cats, null, 2)} as GuideCategory[]

export function findLesson(id: string): { lesson: GuideLesson; category: GuideCategory } | null {
  for (const c of GUIDE) { const l = c.lessons.find(x => x.id === id); if (l) return { lesson: l, category: c } }
  return null
}

// Longest-prefix route → lesson id (for the contextual Help button)
const PATH_LESSONS: [string, string][] = ${JSON.stringify(PATH_LESSONS)}
export function lessonIdForPath(pathname: string, role: UserRole): string | null {
  for (const [prefix, id] of PATH_LESSONS) {
    if (pathname === prefix || pathname.startsWith(prefix + '/')) {
      const hit = findLesson(id)
      if (hit && canSee(role, hit.lesson.minRole)) return id
    }
  }
  return null
}
`
writeFileSync('lib/help/guide-data.ts', ts)
console.log('lib/help/guide-data.ts generated:', Math.round(ts.length/1024), 'KB')
